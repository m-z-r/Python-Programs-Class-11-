# Swap two variables without using a temporary variable

num_1 = 5
num_2 = 3

print("First value = ", num_1, " & Second value = ", num_2)

#usage of tuples
num_1,num_2 = num_2,num_1

#tuples allow easy switch-up
print("After swapping, First value = ",num_1, " & Second value = ", num_2)

